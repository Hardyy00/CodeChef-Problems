
import java.util.*;

class Codechef {
    public static void main(String[] args) {
        Scanner cin = new Scanner(System.in);

        int t = cin.nextInt();

        while (t > 0) {

            int n = cin.nextInt();
            int k = cin.nextInt();

            System.out.println(n - k);

            t--;

        }
        cin.close();
    }
}
